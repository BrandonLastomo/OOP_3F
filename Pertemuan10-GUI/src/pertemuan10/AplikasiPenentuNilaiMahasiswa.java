    private void hitungBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_hitungBtnActionPerformed
        // TODO add your handling code here:
        // memasukkan nilai ke objek nm
        nm.tugas = Double.parseDouble(tugasInput.getText());
        nm.uts = Double.parseDouble(UTSInput.getText());
        nm.uas = Double.parseDouble(UASInput.getText());
        
        // menampilkan hasil input dan perhitungan
        nameContainer.setText(nameInput.getText());
        nilaiContainer.setText(""+nm.nilaiRata());
        gradeContainer.setText(""+nm.gradeMhs());
        hasilContainer.setText(""+nm.hasil());
    }//GEN-LAST:event_hitungBtnActionPerformed

    private void simpanBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_simpanBtnActionPerformed
        // TODO add your handling code here:
        try{
            // siapin file dan nama
            BufferedWriter out = new BufferedWriter(new
            FileWriter("hitungnilai.txt"));
            
            // siapin data
            String hasil = nameContainer.getText()+' ' +'\n'+
             nilaiContainer.getText()+' '+'\n'+
             gradeContainer.getText()+' '+
             hasilContainer.getText();
            
            // masukkan data ke file
            out.write(hasil);
            
            JOptionPane.showMessageDialog(null, "Berhasil disimpan dalam file");
            out.close();
        }catch (Exception e){
            System.err.println("Error : "+e.getMessage());
        }
    }//GEN-LAST:event_simpanBtnActionPerformed

